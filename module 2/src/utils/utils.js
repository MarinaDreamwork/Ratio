export function showNumber() {
  let random = Math.floor * (Math.random() * 100) + 1;
  switch (true) {
    case random < 11: return 4
    default: return 2
  }
}

export function multipleEqualElements(num) {
  let totalValue = num * 2;
  return totalValue;
}

let sum = 0;
export function sumScoreElements(num) {
  sum += num;
}

